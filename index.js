class Car {
    gearbox;
    wheels;
    seats;
    windows;
    sideMirrors;
    engine;
    clutch;
    brake;

    constructor(gearbox, wheels, seats, windows, side_mirrors, engine, clutch, brake) {
        this.gearbox = gearbox;
        this.wheels = wheels;
        this.seats = seats;
        this.windows = windows;
        this.sideMirrors = sideMirrors;
        this.engine = engine;
        this.clutch = clutch;
        this.brake = brake;
    }
}